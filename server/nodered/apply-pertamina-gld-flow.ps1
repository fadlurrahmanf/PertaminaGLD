$apiUsesEnvelope = $false
$existingFlows = @()
if ($current.PSObject.Properties.Name -contains "flows") {
    $apiUsesEnvelope = $true
    $existingFlows = @($current.flows)
} elseif ($current.PSObject.Properties.Name -contains "value") {
    $existingFlows = @($current.value)
} else {
    $existingFlows = @($current)
}

$kept = @($existingFlows | Where-Object {
    $id = [string]$_.id
    -not ($id.StartsWith("pgl_") -or $id -eq $broker)
})
$merged = @($kept + $nodes)

$headers = @{
    "Node-RED-Deployment-Type" = "full"
}

if ($apiUsesEnvelope) {
    $body = @{
        rev = $current.rev
        flows = $merged
        credentials = @{}
    }

    if ($MqttUser -and $MqttPassword) {
        $body.credentials[$broker] = @{
            user = $MqttUser
            password = $MqttPassword
        }
    }
} else {
    $body = $merged
}

$json = $body | ConvertTo-Json -Depth 50
$response = Invoke-RestMethod -Uri $flowsEndpoint -Method Post -Headers $headers -ContentType "application/json" -Body $json -TimeoutSec 60

