    $source = Join-Path $NodeRedUserDir $optionalFile
    if (Test-Path $source) {
        Copy-Item -Path $source -Destination (Join-Path $backupDir "$optionalFile.$timestamp.bak") -Force
    }
}

$flowsEndpoint = "$NodeRedUrl/flows"
